<b>Garden of Glass</b> is a botania addon. Having installed will allow you to create a <b>skyblock</b> type world.<br>
Aside from allowing you to have a skyblock world, a few items and blocks are added, as well as some recipes in botania tweaked to fit the skyblock.<br>
The goal of this addon is to allow for every single item in the game to be acquired properly in a skyblock evironment.<br>
The <b>GardenOfGlass.jar</b> file required to play this mode is simply an empty mod, used to signal to the base Botania mod to enable the GoG features.<br>
This jar doesn't need to be updated, only the base Botania one does. In order for Garden of Glass to work, you need at least <b>Botania r1.7-199</b>.<br><br>
